<?php

// Задание 1: Определение четности чисел

// Создаем массив из 5 чисел
$numbers = [10, 15, 22, 33, 42];

// Используем цикл foreach для перебора элементов массива
foreach ($numbers as $number) {
    // Проверяем, является ли число четным или нечетным
    if ($number % 2 == 0) {
        echo "Число $number — четное\n";
    } else {
        echo "Число $number — нечетное\n";
    }
}

//Задание 2: Подсчет положительных и отрицательных чисел

// Создаем массив из 7 чисел, содержащих как положительные, так и отрицательные значения
$numbers = [10, -5, 2, -3, 0, 7, -1];

// Инициализируем счетчики для положительных и отрицательных чисел
$positiveCount = 0;
$negativeCount = 0;

// Используем цикл foreach для перебора элементов массива
foreach ($numbers as $number) {
    // Проверяем, является ли число положительным или отрицательным
    if ($number > 0) {
        $positiveCount++; // Увеличиваем счетчик положительных чисел
    } elseif ($number < 0) {
        $negativeCount++; // Увеличиваем счетчик отрицательных чисел
    }
}

// Выводим результаты
echo "Положительных чисел: $positiveCount\n";
echo "Отрицательных чисел: $negativeCount\n";

// Задание 3: Таблица умножения

// Цикл for, который перебирает значения от 1 до 10
for ($i = 1; $i <= 10; $i++) {
    // Формируем строку с результатом умножения и выводим её
    echo "5 * $i = " . (5 * $i) . "<br>";
}

// Задание 4: Условное отображение оценок студентов

// Создаем массив с именами студентов и их оценками
$students = [
    "Иван" => 85,
    "Мария" => 74,
    "Алексей" => 40
];

// Используем цикл foreach для обработки каждого студента
foreach ($students as $name => $score) {
    // Проверяем, сдал ли студент экзамен
    if ($score >= 50) {
        echo "$name сдал экзамен!<br>";
    } else {
        echo "$name не сдал экзамен!<br>";
    }
}

// Задание 5: Поиск минимального и максимального значений

// Создаем массив из 8 случайных чисел от 1 до 100
$array = [];
for ($i = 0; $i < 8; $i++) {
    $array[] = rand(1, 100);
}

// Инициализируем переменные min и max первым элементом массива
$min = $array[0];
$max = $array[0];

// Используем цикл foreach для поиска минимального и максимального значений
foreach ($array as $number) {
    if ($number < $min) {
        $min = $number; // Обновляем минимальное значение
    }
    if ($number > $max) {
        $max = $number; // Обновляем максимальное значение
    }
}

// Выводим результаты
echo "Минимальное число: $min\n";
echo "Максимальное число: $max\n";

?>
